echo "→ Creando enlaces simbólicos..."
  ln -sf "default" "$D/left_ptr"
  ln -sf "default" "$D/arrow"
  ln -sf "default" "$D/top_left_arrow"
  ln -sf "pointer" "$D/hand"
  ln -sf "pointer" "$D/hand2"
  ln -sf "pointer" "$D/pointing_hand"
  ln -sf "pointer" "$D/e29285e634086352946a0e7090d73106"
  ln -sf "help" "$D/question_arrow"
  ln -sf "help" "$D/5c6cd98b3f3ebcb1f9af7d374e6ef9e2"
  ln -sf "help" "$D/d9ce0ab605698f320427677b458ad60b"
  ln -sf "progress" "$D/left_ptr_watch"
  ln -sf "progress" "$D/08e8e1c95fe2fc01f976f1e063a24ccd"
  ln -sf "progress" "$D/3ecb610c1bf2410f44200f48c40d3599"
  ln -sf "wait" "$D/watch"
  ln -sf "wait" "$D/0426c94ea35c87780ff01dc239897213"
  ln -sf "crosshair" "$D/cross"
  ln -sf "crosshair" "$D/tcross"
  ln -sf "text" "$D/xterm"
  ln -sf "text" "$D/ibeam"
  ln -sf "pencil" "$D/draft"
  ln -sf "not-allowed" "$D/crossed_circle"
  ln -sf "not-allowed" "$D/forbidden"
  ln -sf "not-allowed" "$D/03b6e0fcb3499374a867c041f52298f0"
  ln -sf "move" "$D/fleur"
  ln -sf "move" "$D/size_all"
  ln -sf "move" "$D/9081237383d90e509aa00f00170e968f"
  ln -sf "ew-resize" "$D/sb_h_double_arrow"
  ln -sf "ew-resize" "$D/h_double_arrow"
  ln -sf "ew-resize" "$D/size_hor"
  ln -sf "ew-resize" "$D/left_side"
  ln -sf "ew-resize" "$D/right_side"
  ln -sf "ns-resize" "$D/sb_v_double_arrow"
  ln -sf "ns-resize" "$D/v_double_arrow"
  ln -sf "ns-resize" "$D/size_ver"
  ln -sf "ns-resize" "$D/top_side"
  ln -sf "ns-resize" "$D/bottom_side"
  ln -sf "nwse-resize" "$D/bd_double_arrow"
  ln -sf "nwse-resize" "$D/c7088f0f3e6c8088236ef8e1e3e70000"
  ln -sf "nwse-resize" "$D/size_fdiag"
  ln -sf "nesw-resize" "$D/fd_double_arrow"
  ln -sf "nesw-resize" "$D/fcf1c3c7cd4491d801f1e1c78f100000"
  ln -sf "nesw-resize" "$D/size_bdiag"
  ln -sf "col-resize" "$D/split_h"
  ln -sf "row-resize" "$D/split_v"
  ln -sf "alias" "$D/link"
  ln -sf "alias" "$D/dnd-link"
  ln -sf "alias" "$D/3085a0e285430894940527032f8b26df"
  ln -sf "cell" "$D/plus"
  ln -sf "cell" "$D/054_plus"
  ln -sf "grab" "$D/openhand"
  ln -sf "grab" "$D/hand1"
  ln -sf "grabbing" "$D/closedhand"
  ln -sf "grabbing" "$D/dnd-move"
  ln -sf "grabbing" "$D/fcf21c00b30f7e3f83fe0dfd12e71cff"
  ln -sf "copy" "$D/1081e37283d90000800003c07f3ef6bf"
  ln -sf "copy" "$D/6407b0e94181790501fd1e167b474872"
  ln -sf "no-drop" "$D/dnd-no-drop"
  ln -sf "no-drop" "$D/03b6e0fcb3499374a867c041f52298f0"
  ln -sf "zoom-in" "$D/f41c0e382c94c0958e07017e42b00462"
  ln -sf "zoom-out" "$D/f41c0e382c94c0958e07017e42b00463"
  ln -sf "clock" "$D/watch"
echo "→ Aplicando tema..."
mkdir -p "$HOME/.config/gtk-3.0"
if grep -q gtk-cursor-theme-name "$HOME/.config/gtk-3.0/settings.ini" 2>/dev/null; then
  sed -i "s/gtk-cursor-theme-name=.*/gtk-cursor-theme-name=$THEME/" "$HOME/.config/gtk-3.0/settings.ini"
else
  printf "[Settings]\ngtk-cursor-theme-name=$THEME\n" >> "$HOME/.config/gtk-3.0/settings.ini"
fi
command -v kwriteconfig5 &>/dev/null && kwriteconfig5 --file kcminputrc --group Mouse --key cursorTheme "$THEME"
command -v gsettings &>/dev/null && gsettings set org.gnome.desktop.interface cursor-theme "$THEME" 2>/dev/null
echo "Xcursor.theme: $THEME" | xrdb -merge 2>/dev/null
echo ""
echo "✓ Instalado. Reinicia sesión para aplicar los cambios."
